
public class Ex_12_5 {

	public static void main(String[] args) {
		
		for (int i=1; i<=10; i++) {//תחילה הספרה היא 1
			
			for (int j=1;j<=10;j++)//לאחר שהכפיל את כל הספרות j עולה ב 1 ואז הוא חוזר ל for הראשון
				System.out.print(i*j+"\t");//מכפיל את 1 בכל הספרות עד 10 וכך הלאה...מכפיל את 2 בכל הספרות עד 10 וכך הלאה
			//ויש שמירה בין המספרים באמצעות מרווח tab
			
			System.out.println();// יורד שורה אחת למטה אחרי שהוא מחשב את כל ה מספרים
		}
		}
}
